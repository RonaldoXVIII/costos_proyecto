    private void bt_calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_calcularActionPerformed
        for(Proceso proc : proceso){
            proc.calculoCifaActividad((a,b)->a.getImpulsador().getNombre().equals(b.getNombre()));
            at_reporte.append("\nDatos del Producto: "+proc.getNombreProducto()+
                    "\n N° Unidades: "+proc.getNumeroUnidades()+"\nCosto MaterialD: "+
                    proc.getCostoMaterialD()+"\nCosto Mano de ObraD: "+proc.getCostoManoObraD());
            
            
            for(int i=0;i<proc.getActividadesUsadas().size();i++){
                at_reporte.append("\nLa actividad "+proc.getActividadesUsadas().get(i).getNombreActividad()+" : "+proc.getActividadesUsadas().get(i).getCostoUnidadImpulsador()+" X "+proc.getImpulsadorUsados().get(i).getCantidad()+" = "+proc.getCifaActividad().get(i)+"\n");
            }
            at_reporte.append("\nEl cifa total es: "+proc.calculoCifaTotal()+"\n");
            
            at_reporte.append("El costo total del producto es: "+proc.calculoCostoTotal()+"\n");
            at_reporte.append("El costo unitario del producto es: "+proc.calculoCostoUnitario()+"\n");
            at_reporte.append("--------------------------------------------------------");
        }
        
        
        bt_reporte.setVisible(true);
    }//GEN-LAST:event_bt_calcularActionPerformed


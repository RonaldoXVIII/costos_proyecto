    private void bt_añadirProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_añadirProductoActionPerformed
        String productoNom;
        double costoMaterial,costoManoObra;
        int numeroUnidades;
        if(!txt_productoNom.getText().equals("") && !txt_productoUnidades.getText().equals("") && !txt_productoCosMaterial.getText().equals("") && !txt_productoCosManoObra.getText().equals("")){
            productoNom=txt_productoNom.getText();
            costoManoObra=Double.parseDouble(txt_productoCosManoObra.getText());
            costoMaterial=Double.parseDouble(txt_productoCosMaterial.getText());
            numeroUnidades=Integer.parseInt(txt_productoUnidades.getText());
            Proceso process=new Proceso(costoMaterial, costoManoObra, productoNom, numeroUnidades);
            process.setActividadesUsadas(actividades);
            process.setImpulsadorUsados(impulsadores);
            proceso.add(process);
            limpiarProducto();
            llenarCb_actividadUsada();
            System.out.println("Producto añadido Correctamente");
            actividades=null;
            impulsadores=null;
            actividades=new ArrayList();
            impulsadores=new ArrayList();
        }else{
            limpiarProducto();
            System.out.println("Complete los datos");
        }
        
        
    }//GEN-LAST:event_bt_añadirProductoActionPerformed

    private void bt_asignarAProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_asignarAProductoActionPerformed
        String impulsadorNom;
        double impulsadorCant;
        if(!txt_cantidadImpulsadorProducto.getText().isEmpty() && !cb_actividadUsada.getSelectedItem().equals("SELECCIONE")){
            impulsadorNom=cb_impulsadorUsado.getSelectedItem().toString();
            impulsadorCant=Double.parseDouble(txt_cantidadImpulsadorProducto.getText());
            Impulsador impul=new Impulsador(impulsadorNom,impulsadorCant);
            impulsadores.add(impul);
            for(Actividad act : actividadesTotales){
                if(act.getImpulsador().getNombre().equals(impul.getNombre())){
                    actividades.add(act);
                    cb_actividadUsada.removeItem(act.getNombreActividad());
                    cb_actividadUsada.setSelectedIndex(0);
                    cb_impulsadorUsado.removeAllItems();
                    cb_impulsadorUsado.addItem("-----");
                    cb_impulsadorUsado.setSelectedIndex(0);
                }
            }
            txt_cantidadImpulsadorProducto.setText("");
            
            System.out.println("Asignacion de Actividades al Producto Exitosa");
        }else{
            System.out.println("Complete todos los datos");
        }
        
    }//GEN-LAST:event_bt_asignarAProductoActionPerformed


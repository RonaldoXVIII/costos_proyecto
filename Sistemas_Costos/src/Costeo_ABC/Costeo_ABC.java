    private void bt_ingresarActividadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_ingresarActividadActionPerformed
        String NomActividad,NomImpulsador;
        double CifActividad,CantImpulsador;
        if(!txt_actividadNom.getText().equals("") && !txt_actividadCif.getText().equals("") && !txt_impulsadorActividad.getText().equals("") && !txt_impulsadorActividadCant.getText().equals("")){
            System.out.println("Aviso de Ingreso Actividades:\nActividades con todos los datos\n");
            NomActividad=txt_actividadNom.getText();
            CifActividad=Double.parseDouble(txt_actividadCif.getText());
            NomImpulsador=txt_impulsadorActividad.getText();
            CantImpulsador=Double.parseDouble(txt_impulsadorActividadCant.getText());
            Actividad act=new Actividad(NomActividad, CifActividad, new Impulsador(NomImpulsador, CantImpulsador));
            act.costoUnidadImpulsador();
            actividadesTotales.add(act);
            cb_actividadUsada.addItem(act.getNombreActividad());
            limpiarActividad();
        }else{
            System.out.println("Aviso de Ingreso Actividades:\nfaltan datos de las actividades\n");
            limpiarActividad();
        }
        
    }//GEN-LAST:event_bt_ingresarActividadActionPerformed


    private void llenarCb_actividadUsada(){
        int num=cb_actividadUsada.getItemCount();
        for(int i=1;i<num;i++){
            cb_actividadUsada.removeItemAt(i);
        }
        
        for(Actividad act : actividadesTotales){
            
            cb_actividadUsada.addItem(act.getNombreActividad());
        }
        
    }
    
